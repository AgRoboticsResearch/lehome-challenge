#!/usr/bin/env python
"""
Verify Evo-RL dataset format generated by evaluation script.
"""

import json
from pathlib import Path
from lerobot.datasets.lerobot_dataset import LeRobotDataset


def verify_dataset(dataset_path: str):
    """Verify that the dataset has Evo-RL compatible format."""

    print("=" * 60)
    print("Evo-RL Dataset Verification")
    print("=" * 60)
    print(f"\nDataset path: {dataset_path}")

    dataset_path = Path(dataset_path)

    # Handle get_next_experiment_path_with_gap() structure
    # If the path contains numbered subdirectories (001, 002, etc.), use the latest one
    if dataset_path.is_dir():
        subdirs = sorted([d for d in dataset_path.iterdir() if d.is_dir() and d.name.isdigit()])
        if subdirs:
            latest_dir = subdirs[-1]
            print(f"Found multiple experiment directories, using latest: {latest_dir.name}")
            dataset_path = latest_dir

    print(f"Loading from: {dataset_path}")

    # Load the dataset - for local datasets, we need to find the actual repo_id
    # Check for info.json to get the repo_id
    info_path = dataset_path / "meta" / "info.json"
    if not info_path.exists():
        print(f"❌ Dataset metadata not found at: {info_path}")
        print(f"   Make sure the evaluation completed successfully.")
        return

    import json
    with open(info_path) as f:
        info = json.load(f)
        repo_id = info.get("repo_id", "lehome_eval")
        total_episodes = info.get("total_episodes", 0)

    print(f"\nDataset info from info.json:")
    print(f"  repo_id: {repo_id}")
    print(f"  total_episodes: {total_episodes}")
    print(f"  fps: {info.get('fps', 'N/A')}")

    if total_episodes == 0:
        print(f"\n⚠️  Dataset has 0 episodes!")
        print(f"   This means no episodes were saved during evaluation.")
        print(f"   Possible reasons:")
        print(f"     - Evaluation is still running")
        print(f"     - Evaluation crashed before saving")
        print(f"     - save_mode filtered out all episodes")
        return

    # Load the dataset with the correct repo_id and root path
    # For numbered subdirectories, use the directory name as repo_id
    actual_root = dataset_path.parent
    actual_repo_id = dataset_path.name

    print(f"\nLoading dataset:")
    print(f"  root: {actual_root}")
    print(f"  repo_id: {actual_repo_id}")

    try:
        ds = LeRobotDataset(actual_repo_id, root=actual_root)
        print(f"\n✅ Dataset loaded successfully")
        print(f"   Repo ID: {actual_repo_id}")
        print(f"   Episodes: {len(ds)}")
        print(f"   FPS: {ds.fps}")
    except Exception as e:
        print(f"❌ Failed to load dataset: {e}")
        import traceback
        traceback.print_exc()
        return

    # Check features
    print("\n" + "-" * 60)
    print("Dataset Features:")
    print("-" * 60)

    evo_rl_features = [
        "complementary_info.policy_action",
        "complementary_info.is_intervention",
        "complementary_info.state",
        "complementary_info.collector_policy_id",
    ]

    for feature in evo_rl_features:
        if feature in ds.features:
            feat_info = ds.features[feature]
            print(f"✅ {feature}")
            print(f"   dtype: {feat_info['dtype']}, shape: {feat_info.get('shape', 'N/A')}")
        else:
            print(f"❌ {feature} - MISSING")

    # Check episode metadata
    print("\n" + "-" * 60)
    print("Episode Metadata:")
    print("-" * 60)

    if len(ds.episodes) > 0:
        for i, ep in enumerate(ds.episodes[:3]):  # Check first 3 episodes
            print(f"\nEpisode {i}:")
            print(f"  index: {ep['episode_index']}")
            if 'episode_success' in ep:
                print(f"  ✅ episode_success: {ep['episode_success']}")
            else:
                print(f"  ❌ episode_success: MISSING")

            # Check length
            start_idx = ep['start_index']
            end_idx = ep['end_index']
            num_frames = end_idx - start_idx
            print(f"  frames: {num_frames}")

    # Check frame-level data
    print("\n" + "-" * 60)
    print("Frame-Level Data Sample:")
    print("-" * 60)

    if len(ds) > 0:
        # Check first frame of first episode
        frame = ds[0]
        print(f"\nFrame 0 (first frame of first episode):")

        for feature in evo_rl_features:
            if feature in frame:
                value = frame[feature]
                print(f"  ✅ {feature}: {value}")
                # Print shape and dtype
                if hasattr(value, 'shape'):
                    print(f"     shape={value.shape}, dtype={value.dtype}")
            else:
                print(f"  ❌ {feature}: MISSING")

    # Check statistics across dataset
    print("\n" + "-" * 60)
    print("Dataset Statistics:")
    print("-" * 60)

    total_frames = 0
    intervention_count = 0
    success_count = 0

    for ep in ds.episodes:
        total_frames += (ep['end_index'] - ep['start_index'])
        if ep.get('episode_success') == 'success':
            success_count += 1

    # Count interventions from frames
    for i in range(len(ds)):
        frame = ds[i]
        if 'complementary_info.is_intervention' in frame:
            if frame['complementary_info.is_intervention'][0] > 0.5:  # float32
                intervention_count += 1

    print(f"\n  Total episodes: {len(ds.episodes)}")
    print(f"  Successful episodes: {success_count}")
    print(f"  Total frames: {total_frames}")
    print(f"  Intervention frames: {intervention_count}")
    if total_frames > 0:
        print(f"  Intervention rate: {100 * intervention_count / total_frames:.2f}%")

    # Check for video files
    print("\n" + "-" * 60)
    print("Video Files:")
    print("-" * 60)

    videos_dir = Path(dataset_path) / "videos"
    if videos_dir.exists():
        video_files = list(videos_dir.glob("*.mp4"))
        print(f"  ✅ Found {len(video_files)} video files")
        for vid in video_files[:5]:  # Show first 5
            print(f"     - {vid.name}")
    else:
        print(f"  ❌ No videos directory found")

    # Final verdict
    print("\n" + "=" * 60)
    print("Summary:")
    print("=" * 60)

    all_features_present = all(f in ds.features for f in evo_rl_features)
    has_episode_success = len(ds.episodes) > 0 and 'episode_success' in ds.episodes[0]

    if all_features_present and has_episode_success:
        print("✅ Dataset is Evo-RL compatible!")
        print("\nYou can now use this dataset for:")
        print("  1. Value function training: lerobot-value-train")
        print("  2. ACP label generation: lerobot-value-infer")
    else:
        print("❌ Dataset is NOT fully Evo-RL compatible")
        if not all_features_present:
            print("   Missing complementary_info features")
        if not has_episode_success:
            print("   Missing episode_success metadata")

    print("=" * 60)


if __name__ == "__main__":
    import sys

    if len(sys.argv) < 2:
        print("Usage: python scripts/verify_evo_rl_dataset.py <dataset_path>")
        print("\nExample:")
        print("  python scripts/verify_evo_rl_dataset.py Datasets/test_evo_rl/pant_long")
        sys.exit(1)

    dataset_path = sys.argv[1]
    verify_dataset(dataset_path)
